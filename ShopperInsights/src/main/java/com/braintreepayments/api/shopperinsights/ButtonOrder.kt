package com.braintreepayments.api.shopperinsights

/**
 * The order or ranking in which payment buttons appear.
 */
enum class ButtonOrder {

    /**
     * First place
     */
    FIRST,

    /**
     * Second place
     */
    SECOND,

    /**
     * Third place
     */
    THIRD,

    /**
     * Fourth place
     */
    FOURTH,

    /**
     * Fifth place
     */
    FIFTH,

    /**
     * Sixth place
     */
    SIXTH,

    /**
     * Seventh place
     */
    SEVENTH,

    /**
     * Eighth place
     */
    Eights,

    /**
     * Greater than Eighth place
     */
    OTHER
}